# 字符串切片
# 字符串的索引操作，只能使用[]下标访问，不能修改
# str[开始值：结束值：步进值]
var1 = '君不见，黄河之水天上来，奔流到海不复回。'
# 全部输出
print(var1[::])  # 君不见，黄河之水天上来，奔流到海不复回。
# 只取一个下标的值
print(var1[5])  # 河
# 取开始-结束内的下标的值，不包含末尾
print(var1[1:5])  # 不见，黄
# 从下标2开始取，一直到下标为10之前，能取到1，取不到10
print(var1[2:10:2])  # 不，河水上，
# 取全部的字符串，隔一个取一个
print(var1[::2])  # 君见黄之天来奔到不回
# 取全部的字符串，字符串倒过来
print(var1[::-1])  # 。回复不海到流奔，来上天水之河黄，见不君，
# 从初始值那里开始一直到末尾的字符串，
print(var1[1::1])  # 不见，黄河之水天上来，奔流到海不复回。
# 从初始值那里开始一直到末尾的字符串，隔一个取一个
print(var1[1::2])  # 不，河水上，流海复。
# 从初始值那里开始一直到末尾的字符串，倒着取
print(var1[1::-2])  # 不
print(var1[10::-2])  # 来天之黄见君

