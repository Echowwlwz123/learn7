# 字符串格式化方法
# format()格式化字符串
# 1.普通方式
a = '李白'
vars = '{}乘舟将欲行，忽闻岸上踏歌声'.format(a)
print(vars)  # 李白乘舟将欲行，忽闻岸上踏歌声
# 2.索引传参
vars = '{1}乘舟将欲行，忽闻岸{2}上踏歌声{0}'.format('a', 'b', 'c')
print(vars)  # b乘舟将欲行，忽闻岸c上踏歌声a
# 3.关键字传参
vars = '{a}乘舟将欲行，忽闻岸{b}上踏歌声{c}'.format(a='李白', b='gesheng', c='huhai')
print(vars)  # 李白乘舟将欲行，忽闻岸gesheng上踏歌声huhai
# 4.容器传参
vars = '豪放派：{0[0]},婉约派：{0[1]}，蛋黄派：{0[2]}'.format(['李白', '辛弃疾', '达利园'])
print(vars)  # 豪放派：李白,婉约派：辛弃疾，蛋黄派：达利园
data = {'a': '辛弃疾', 'b': '李白'}

vars = '{a}乘舟将欲行，忽闻岸{b}上踏歌声'.format(**data)
print(vars)  # 辛弃疾乘舟将欲行，忽闻岸李白上踏歌声

vars = f'{data["a"]}乘舟将欲行，忽闻岸{data["b"]}上踏歌声'
print(vars)  # 辛弃疾乘舟将欲行，忽闻岸李白上踏歌声

vars = '圆周率是多少：{:.2f}'.format(3.1415926)
print(vars)  # 圆周率是多少：3.14 限定小数位数
