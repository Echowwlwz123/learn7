class Caculator():
    # 加法
    def add(self, a, b):
        return a + b

    # 减法
    def subtr(self, a, b):
        return a - b

    # 乘法
    def multi(self, a, b):
        return a * b

    # 除法
    def divi(self, a, b):
        return a / b
